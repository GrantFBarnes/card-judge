CREATE
OR REPLACE PROCEDURE SP_RESET_RESPONSES(IN VAR_PLAYER_ID UUID)
BEGIN
    DECLARE VAR_LOBBY_ID UUID;

    SELECT
        LOBBY_ID
    INTO
        VAR_LOBBY_ID
    FROM PLAYER
    WHERE ID = VAR_PLAYER_ID;

    UPDATE PLAYER
    SET EXTRA_RESPONSES = 0
    WHERE LOBBY_ID = VAR_LOBBY_ID;

    CALL SP_SET_RESPONSES_LOBBY(VAR_LOBBY_ID);
END;